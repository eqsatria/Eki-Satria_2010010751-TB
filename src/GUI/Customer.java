    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM customer ORDER BY ID_Customer DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String IdCustomer = r.getString("ID_Customer").substring(2);
                String C = "" +(Integer.parseInt(IdCustomer)+1);
                String Nol = "";
                
                if (C.length()==1)
                {Nol = "00";}
                else if (C.length()== 2)
                {Nol = "0";}
                else if(C.length()== 3)
                {Nol = "";}
                
                txtidpembeli.setText("C" + Nol + C);
            }else{
                txtidpembeli.setText("C001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
        
